num = int(input('Digite o número do dia da semana:\n'))
if num == 1:
    dia = 'Domingo'
elif num == 2:
    dia = 'Segunda'
elif num == 3:
    dia = 'Terça'
elif num == 4:
    dia = 'Quarta'
elif num == 5:
    dia = 'Quinta'
elif num == 6:
    dia = 'Sexta'
elif num == 7:
    dia = 'Sabado'
else:
    dia = 'Valor inválido'
print(dia)
